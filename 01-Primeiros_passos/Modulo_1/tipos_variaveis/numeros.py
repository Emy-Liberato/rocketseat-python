#Ineteiro
numero_inteiro = 3
print("inteiro = ", numero_inteiro)

#real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante", numero_real)

#type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

#soma +
soma = 1 + 1
print("1 + 1 = ", soma)

#subtração - 
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

#multiplicação *
multiplicacao = 2 * 2
print("2 X 2 = ", multiplicacao)

#duvisão
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variavel do resultado da divisão =", type(divisao))

#int()
print("Valor em inteiro = ", int(divisao))

#float()
print("Valor em float = ", float(soma))

#Modulo %
modulo = 5 % 2
print("5 % 2 = ", modulo)

#duvisão inteiro
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variavel do resultado da divisão =", type(divisao))